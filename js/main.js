  var shopFilter = $('.filter-button li'),
      shopWork = $('.all-other');
      $(shopFilter).click(function(){
        var value = $(this).attr('data-filter');
        if(value === "all") {
          $(shopWork).show('80');
        } else {
          //Hide shop work
          $(shopWork).not('.'+value).hide('80');
          //Show shop work
          $(shopWork).filter('.'+value).show('80');
        }
        //Add / remove active class from filter button
        $(this).addClass('active').siblings().removeClass('active');
      });


